    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        File filePath = null;
        if ("".equals(this.UserIDInput.getText())||"".equals(this.PasswordInput.getText())||"".equals(this.NameInput.getText())||"".equals(this.AddressInput.getText())||"".equals(this.EmailInput.getText())){
            JOptionPane.showMessageDialog(null,"Please insert all the data!");
        }
        else{
        String Position = (String) UserPositionComboBox.getSelectedItem();
        if ("Admin".equals(Position)){
            
            filePath = file.searchFilePath(admin.getFileName());
            
        }
        else if ("Product Manager".equals(Position)){
            filePath = file.searchFilePath(pm.getFileName());
        }
        else if ("Supplier".equals(Position)){
            filePath = file.searchFilePath(sp.getFileName());
        }
        
        String userID = UserIDInput.getText();  
        String userPass = PasswordInput.getText();String userName= NameInput.getText();String userAdd= AddressInput.getText(); String userEmail= EmailInput.getText();String userStatus= "Active";
        String [] details ={userID,userPass,userName,userAdd,userEmail,userStatus};
        file.writeDetails(filePath, details,6);
        }
        JOptionPane.showMessageDialog(null,"Successfully registered new user!");
        this.UserIDInput.setText(null);this.PasswordInput.setText(null);this.NameInput.setText(null);this.AddressInput.setText(null);this.EmailInput.setText(null);
    }//GEN-LAST:event_SubmitButtonActionPerformed

