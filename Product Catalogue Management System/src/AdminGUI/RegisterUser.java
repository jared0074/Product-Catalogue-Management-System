    private void GenerateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerateButtonActionPerformed
        File filePath;String generatedID;
        String Position = (String) UserPositionComboBox.getSelectedItem();
        if (Position == "Admin"){
            admin.setFileName("Admin.txt");
            filePath = file.searchFilePath(admin.getFileName());
            try {
                generatedID = admin.generateUserID(filePath);
                this.UserIDInput.setText(generatedID);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(RegisterUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if (Position == "Product Manager"){
            
            pm.setFileName("ProductManager.txt");
            filePath = file.searchFilePath(pm.getFileName());
            try {
                generatedID = pm.generateUserID(filePath);
                this.UserIDInput.setText(generatedID);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(RegisterUser.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }
        else if (Position == "Supplier"){
            sp.setFileName("Supplier.txt");
            filePath = file.searchFilePath(sp.getFileName());
            try {
                generatedID = sp.generateUserID(filePath);
                this.UserIDInput.setText(generatedID);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(RegisterUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_GenerateButtonActionPerformed

